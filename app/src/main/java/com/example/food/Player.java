package com.example.food;

public class Player {

	String name;
	int foodIndex;
	int img;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getFoodIndex() {
		return foodIndex;
	}

	public void setFoodIndex(int foodIndex) {
		this.foodIndex = foodIndex;
	}

	public int getImg() {
		return img;
	}

	public void setImg(int img) {
		this.img = img;
	}

}
